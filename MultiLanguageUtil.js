MultiLanguageUtil.getI18NResourceNameFrom = function (resourceName) {
    var resourcePureName = resourceName.split(".")[0];
    var resourceExtensionName = resourceName.split(".")[1];
    var currentLanguageType = GameDataManager.languageType;
    if (currentLanguageType == LANGUAGE_CHINESE) return "%%.%".format(resourcePureName, "-zh", resourceExtensionName);
    if (currentLanguageType == LANGUAGE_JAPANESE) return "%%.%".format(resourcePureName, "-ja", resourceExtensionName);
    return "%%.%".format(resourcePureName, "-en", resourceExtensionName);    
};

